<?php

// Return the Sum of Two Numbers #1

function sum($x, $y) {
	return $x + $y;
}

echo "Sum of Two Numbers 5 and 6: " . sum(5, 6);

echo "\r\n"; // Break to new line

// Return the Subtraction of Two Numbers #2
function subtract($x, $y) {
	return $x - $y;
}

echo "Subtraction of Two Numbers 6 from 5: " . subtract(6, 5);

?>
